		LoadCityRuinsData()
		LoadConfigData("CityRuins")
		
	elseif (mapname == "rp_zaton_evening" or mapname == "rp_zaton_day" or mapname == "rp_zaton_night") then
	
		LoadZatonEveningData()
		
		if (mapname == "rp_zaton_evening") then
			LoadConfigData("ZatonEvening")
